def ensure_dir(path: Path) -> Path:
    path.mkdir(parents=True, exist_ok=True)
    return path


def write_csv(path: Path, rows: List[Dict[str, object]]) -> None:
    if not rows:
        return
    keys: List[str] = []
    for row in rows:
        for key in row:
            if key not in keys:
                keys.append(key)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=keys, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def spice_num(value: float) -> str:
    if value == 0.0:
        return "0"
    return f"{value:.12g}"


def safe_float(value: object, default: float = 0.0) -> float:
    try:
        result = float(value)  # type: ignore[arg-type]
    except (TypeError, ValueError):
        return default
    return result if math.isfinite(result) else default


def sh_quote(value: str) -> str:
    return "'" + value.replace("'", "'\"'\"'") + "'"


def resolve_ngspice_path(raw_path: str | None) -> str | None:
    if raw_path:
        if raw_path.startswith("wsl:"):
            tool = raw_path.split(":", 1)[1] or "ngspice"
            probe = subprocess.run(
                ["wsl", "-e", "sh", "-lc", f"command -v {sh_quote(tool)} >/dev/null 2>&1"],
                text=True,
                capture_output=True,
                timeout=30,
                check=False,
            )
            return raw_path if probe.returncode == 0 else None
        candidate = Path(raw_path)
        if candidate.exists():
            return str(candidate)
        return shutil.which(raw_path)
    return spice_base.resolve_ngspice_path(None)


def n(node_idx: int) -> str:
    return f"n{node_idx}"


def cell_values(case: NLTLCase) -> Dict[str, float]:
    dx = case.total_length_m / case.cell_count
    vp = case.phase_velocity_m_s
    z0 = case.z0_ohm
    l_cell = z0 * dx / vp
    c_cell = dx / (z0 * vp)
    c_var_eff = c_cell * case.nonlinear_fraction
    c_fixed = max(c_cell - c_var_eff, c_cell * 0.03)
    cjo = c_var_eff * (1.0 + case.bias_v / max(case.varactor_vj_v, EPS)) ** case.varactor_m
    r_series = 0.035 * case.series_loss_ohm_scale * math.sqrt(max(z0, 1.0) / 50.0) * (32.0 / case.cell_count)
    fc_hz = vp / (math.pi * dx)
    return {
        "dx_m": dx,
        "l_cell_h": l_cell,
        "c_cell_f": c_cell,
        "c_fixed_f": 0.0 if case.fixed_cap_only else c_fixed,
        "c_linear_control_f": c_cell,
        "c_var_eff_f": 0.0 if case.fixed_cap_only else c_var_eff,
        "cjo_f": 0.0 if case.fixed_cap_only else cjo,
        "r_series_ohm": r_series,
        "fc_hz": fc_hz,
        "estimated_z0_ohm": math.sqrt(max(l_cell, EPS) / max(c_cell, EPS)),
        "phase_velocity_m_s": dx / math.sqrt(max(l_cell * c_cell, EPS)),
    }


