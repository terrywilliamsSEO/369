@dataclass(frozen=True)
class VaractorModel:
    cjo_f: float
    vj_v: float = 0.75
    m: float = 0.50
    rs_ohm: float = 0.85
    bv_v: float = 18.0
    ibv_a: float = 1.0e-6
    is_a: float = 1.0e-15


@dataclass(frozen=True)
class NLTLCase:
    case_id: str
    name: str
    filename: str
    role: str
    cell_count: int
    z0_ohm: float
    total_length_m: float = TOTAL_LENGTH_M
    phase_velocity_m_s: float = PHASE_VELOCITY_M_S
    source_amplitude_v: float = 2.0
    direct_generated_amplitude_v: float = 0.0
    bias_v: float = 4.0
    nonlinear_fraction: float = 0.82
    varactor_vj_v: float = 0.75
    varactor_m: float = 0.50
    varactor_rs_ohm: float = 0.85
    varactor_bv_v: float = 18.0
    varactor_ibv_a: float = 1.0e-6
    series_loss_ohm_scale: float = 1.0
    shunt_loss_ohm: float = 200_000.0
    target_phase_velocity_scale: float = 1.0
    generated_phase_velocity_scale: float = 1.0
    fixed_cap_only: bool = False
    band_shunts: bool = False
    source_only_drive: bool = True
    direct_100_drive_present: bool = False
    direct_150_drive_present: bool = False
    target_frequency_injection_present: bool = False
